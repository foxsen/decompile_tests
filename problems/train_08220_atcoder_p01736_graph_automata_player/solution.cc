vec gauss_jordan(const mat& A,const vec& b){
  int n=A.size();
  mat B(n,vec(n+1));
  vector<int> can(n,1);
  for(int i=0;i<n;i++)
    for(int j=0;j<n;j++)B[i][j]=A[i][j]; //n*(n+1)?????????????????????

  for(int i=0;i<n;i++)B[i][n]=b[i]; //n???????????£??\

  for(int i=0;i<n;i++){
    int pivot=i;//?¬????????????¨??????????????????
    for(int j=0;j<n;j++)if(can[j] && B[j][i])pivot=j;//0??\??????1????????¢???
    swap(B[i],B[pivot]);//i??????????????£????????????
    if(B[i][i]==1)can[i] = 0;

    
    for(int j=0;j<n;j++){ //
      if(i!=j && B[j][i])//i??????-i?????????????????????
        for(int k=i;k<=n;k++)B[j][k]=B[j][k] ^ B[i][k];//i????????????i????????\??????0????????????
    }
  }
  vec x(n);
  for(int i=0;i<n;i++)x[i]=B[i][n];

  int many = 0;
  for(int i=0;i<n;i++){
    if(B[i][i] == 0) many = 1;
    if(B[i][i] == 0 && B[i][n] == 1) return vec();
  }
  if(many) return vec(n+1);
  return x;
}




mat Mult(const mat &A,const mat &B){
  assert(A[0].size() == B.size());
  int n = A.size(),m = B.size(),l = B[0].size();
  mat C(A.size(),vec(B[0].size(),0));

  for(int i=0;i<n;i++)
    for(int j=0;j<l;j++)
      for(int k=0;k<m;k++) C[i][j]^=(A[i][k]*B[k][j]);
 
  return C;
}

mat pow(mat X,int y){
  assert(X.size() == X[0].size());
  mat res(X.size(),vec(X[0].size()));
  for(int i=0;i<(int)res.size();i++) res[i][i] = 1;
  
  while(y){
    if(y%2) res = Mult(res,X);
    y/=2;
    X = Mult(X,X);

  }
  return res;
}



