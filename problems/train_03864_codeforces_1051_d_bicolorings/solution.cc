#include <bits/stdc++.h>
using namespace std;
long long dp[1001][2001][4];
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  cout.tie(0);
  int n, k;
  cin >> n >> k;
  dp[1][1][0] = 1;
  dp[1][1][1] = 1;
  dp[1][2][2] = 1;
  dp[1][2][3] = 1;
  for (int i = 2; i <= n; i++) {
    for (int j = 1; j <= 2 * i; j++) {
      dp[i][j][0] =
          (dp[i - 1][j][0] % 998244353 + dp[i - 1][j - 1][1] % 998244353 +
           dp[i - 1][j][2] % 998244353 + dp[i - 1][j][3] % 998244353) %
          998244353;
      dp[i][j][1] =
          (dp[i - 1][j - 1][0] % 998244353 + dp[i - 1][j][1] % 998244353 +
           dp[i - 1][j][2] % 998244353 + dp[i - 1][j][3] % 998244353) %
          998244353;
      if (j >= 2)
        dp[i][j][2] =
            (dp[i][j][2] % 998244353 + dp[i - 1][j - 2][3] % 998244353) %
            998244353;
      dp[i][j][2] =
          (dp[i][j][2] % 998244353 + dp[i - 1][j - 1][0] % 998244353 +
           dp[i - 1][j - 1][1] % 998244353 + dp[i - 1][j][2] % 998244353) %
          998244353;
      if (j >= 2)
        dp[i][j][3] =
            (dp[i][j][3] % 998244353 + dp[i - 1][j - 2][2] % 998244353) %
            998244353;
      dp[i][j][3] =
          (dp[i][j][3] % 998244353 + dp[i - 1][j - 1][0] % 998244353 +
           dp[i - 1][j - 1][1] % 998244353 + dp[i - 1][j][3] % 998244353) %
          998244353;
    }
  }
  long long ans = (dp[n][k][0] % 998244353 + dp[n][k][1] % 998244353 +
                   dp[n][k][2] % 998244353 + dp[n][k][3] % 998244353) %
                  998244353;
  cout << ans << endl;
  return 0;
}
